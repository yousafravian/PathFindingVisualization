const directionRow = [-1, +1, 0, 0];
const directionCol = [0, 0, +1, -1];
const start: Point = {x: 0, y: 0};
const end: Point = {x: 5, y: 5};
// tslint:disable-next-line:prefer-const
let visited: boolean[][];
const stack: Array<Point> = [];

visited = new Array<Array<boolean>>(30);
for (let i = 0; i < 30; i++) {
  visited[i] = new Array<boolean>(30);
}


const DFS = (map: string[][], startCell: Point, endCell: Point, stackDFS: Array<Point>): boolean => {

  console.clear();
  for (let i = 0; i < 30; i++) {
    for (let j = 0; j < 30; j++) {
      if (visited[i][j] === true) {
        console.log('X,');
      } else {
        console.log('" ",');
      }
    }
    console.log('\n');
  }

  if (start.x === end.x && start.y === end.y) {
    return;
  }
  stackDFS.push(startCell);

  while (stack.length > 0) {
    for (let i = 0; i < 4; i++) {
      const newR = startCell.x + directionRow[i];
      const newC = startCell.y + directionCol[i];

      // IF OUT OF BOUNDS
      if (newR < 0 || newC < 0) {
        continue;
      }
      if (newR >= map.length || newC >= map[0].length) {
        continue;
      }
      // IF WALL ENCOUNTERED OR VISITED

      if (visited[newR][newC]) {
        continue;
      }
      if (map[newR][newC] === 'W') {
        continue;
      }
      stackDFS.push({x: newR, y: newC});
      visited[newR][newC] = true;

      DFS(map, stackDFS.shift(), endCell, stackDFS);

    }
  }
  return false;
};

